local function safe_register_alias(old_name, new_name)
	if old_name == new_name then
		return
	end
	if minetest.registered_items[old_name]
		or minetest.registered_nodes[old_name]
		or minetest.registered_craftitems[old_name]
		or minetest.registered_tools[old_name]
	then
		return
	end
	if minetest.registered_aliases and minetest.registered_aliases[old_name] then
		return
	end
	minetest.register_alias(old_name, new_name)
end

